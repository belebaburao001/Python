''' Variables ''' 
# A variable is the name given to a memory location. For example

a = 30  # a is a variable & 30 is value stored in it
b = "Python"  # b is a variable & Python is value stored in it

# variable = container to store a value
# Keywords = reserved words in python
# Identifiers = class/function/variable name

''' Single or Double Quotes '''
# String variables can be declared eitherd by usign single or double quotes
x = "python"
x = 'python'
# Both are valid

''' Case - Sensitive '''
# Variable names are case-sensitive
x = 10
X = "20"
# x and X are two different variables