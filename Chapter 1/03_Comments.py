''' Types of Comments in Python '''
# There are two types of comments in python.

# (1) Single Line Comments
# To write a single line comment just add a '#' at the start of the line.

# (2) Multiline Comments
# To write multi-line comments you can use ‘#’ at each line or you can use the three double quots for multiline string (""" """) and also use three times single quotes (''' ''').

"""
example this is a
    multi line
        Comment
"""

'''
example this is a
    multi line
        Comment
'''