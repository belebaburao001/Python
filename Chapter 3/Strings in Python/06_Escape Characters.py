''' Escape Characters in Python '''
# In Python, backslash (\) is used as an escape character.
# It is used to escape special characters in a string like, Double quotes, single quotes, etc.

# Example 01:- Add Double Quotes
str1 = "We all know \"Ragnar Lothbruk\" is the best king of Vikings!"
print(str1)

# Example 02:- Add New Line
str2 = "We all know\nBjorn Lothbrok \nis the son of Lagertha and Ragnar!"
print(str2)

